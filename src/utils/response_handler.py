from flask import jsonify


class ResponseHandler:
    @staticmethod
    def package_result(result, pager=None):
        if isinstance(result, bool):
            return jsonify({
                'success': result
            })
        else:
            return jsonify({
                'data': result,
                'pager': pager
            })
